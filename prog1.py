# Program to demonstrate basic data types in python

no = 10
print("Value of integer variable : ",no," and type : ",type(no))

pi = 3.14
print("Value of floating variable : ",pi," and type : ",type(pi))

strng = "jspm ntc"
print("Value of string variable : ",strng," and type : ",type(strng))

cn = 4+7j
print("Value of complex number : ",cn," and type : ",type(cn))

booln = False
print("Value of boolean variable : ",booln," and type : ",type(booln))    